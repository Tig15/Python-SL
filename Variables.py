# 1. Variable cannot start with a number, it should be alphabet or an underscore.
# 2. Variable is a case sensitive.
# 3. Varible contains number, alphabet and an underscore.

# Camel Type
myName = "Aman"
print(myName)

# Pascal Type
MyName = "Tig"
print(MyName)

# Snake Type
my_name = "Kuro"
print(my_name)

# Many Values to Multiple Variables
x,y,z = "Dawn", "Dusk", "Day"
print(x)
print(y)
print(z)

# One values to Multiple Variables
a = b = c = "Noicee"
print(a)
print(b)
print(c)

# Unpacking, it can be used in list, tuple, set...
Players = ["Kuroko", "Kagami", "Kise"] #list
p1,p2,p3 = Players
print(p1)
print(p2)
print(p3)

