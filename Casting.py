# Casting in Python is done using constructor function :

# 1. int() - Constructs an integer number from integer, float or string literals
x = int(3)
y = int(4.6)
z = int('30')

print(x)
print(y)
print(z)

# 2. float() - Constructs a float number from integer, float or string literals
a, b,c = float(6), float(3.5), float("40")
print(a)
print(b)
print(c)

# 3. str() - Constructs a string from integer, float or string literals
A = str('S1')
B = str('3.48')
C = str('6')
print(A)
print(B)
print(C)